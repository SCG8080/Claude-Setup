// --------------- File Tree Builder ---------------
export interface FileNode {
  name: string;
  type: "file" | "folder";
  children?: FileNode[];
  content?: string;
  path: string;
}

export function buildFileTree(config: ConfigState, selectedDefs: {
  skillDefs: any[];
  subagentDefs: any[];
  ruleDefs: any[];
  hookDefs: any[];
  docDefs: any[];
}): FileNode[] {
  const files: FileNode[] = [];

  if (config.core.includeClaudeMd) {
    files.push({ name: "CLAUDE.md", type: "file", path: "CLAUDE.md", content: generateClaudeMd(config) });
  }
  if (config.core.includeAgentsMd) {
    files.push({ name: "AGENTS.md", type: "file", path: "AGENTS.md", content: generateAgentsMd(config, selectedDefs.subagentDefs) });
  }
  files.push({ name: "README-SETUP.md", type: "file", path: "README-SETUP.md", content: generateReadmeSetup(config) });

  // .claude/ folder
  if (config.core.includeSettingsJson || config.core.includeRules || config.core.includeSkills || config.core.includeSubagents || config.core.includeHooks) {
    const claudeDir: FileNode = { name: ".claude", type: "folder", path: ".claude", children: [] };

    if (config.core.includeSettingsJson) {
      claudeDir.children!.push({ name: "settings.json", type: "file", path: ".claude/settings.json", content: generateSettingsJson(config, selectedDefs.hookDefs) });
    }

    if (config.core.includeRules && selectedDefs.ruleDefs.length > 0) {
      const rulesDir: FileNode = { name: "rules", type: "folder", path: ".claude/rules", children: [] };
      selectedDefs.ruleDefs.forEach((rule) => {
        rulesDir.children!.push({ name: rule.filename, type: "file", path: ".claude/rules/" + rule.filename, content: generateRuleContent(rule.id) });
      });
      claudeDir.children!.push(rulesDir);
    }

    if (config.core.includeSkills && selectedDefs.skillDefs.length > 0) {
      const skillsDir: FileNode = { name: "skills", type: "folder", path: ".claude/skills", children: [] };
      selectedDefs.skillDefs.forEach((skill) => {
        const skillPath = ".claude/skills/" + skill.id;
        const skillDir: FileNode = {
          name: skill.id,
          type: "folder",
          path: skillPath,
          children: [
            { name: "SKILL.md", type: "file", path: skillPath + "/SKILL.md", content: generateSkillMd(skill.id, skill.label) },
          ],
        };
        if (skill.templateFiles?.length > 0) {
          const tplPath = skillPath + "/templates";
          const templatesDir: FileNode = { name: "templates", type: "folder", path: tplPath, children: [] };
          skill.templateFiles.forEach((tf: string) => {
            templatesDir.children!.push({ name: tf, type: "file", path: tplPath + "/" + tf, content: "# " + tf.replace(".md", "").replace(/-/g, " ") + "\n\nTemplate content.\n" });
          });
          skillDir.children!.push(templatesDir);
        }
        skillsDir.children!.push(skillDir);
      });
      claudeDir.children!.push(skillsDir);
    }

    if (config.core.includeSubagents && selectedDefs.subagentDefs.length > 0) {
      const agentsDir: FileNode = { name: "agents", type: "folder", path: ".claude/agents", children: [] };
      selectedDefs.subagentDefs.forEach((agent) => {
        agentsDir.children!.push({ name: agent.id + ".md", type: "file", path: ".claude/agents/" + agent.id + ".md", content: generateSubagentMd(agent.id, agent) });
      });
      claudeDir.children!.push(agentsDir);
    }

    if (config.core.includeHooks && selectedDefs.hookDefs.length > 0) {
      const hooksDir: FileNode = { name: "hooks", type: "folder", path: ".claude/hooks", children: [] };
      selectedDefs.hookDefs.forEach((hook) => {
        hooksDir.children!.push({ name: hook.filename, type: "file", path: ".claude/hooks/" + hook.filename, content: generateHookScript(hook.filename) });
      });
      claudeDir.children!.push(hooksDir);
    }

    files.push(claudeDir);
  }

  // docs/ folder
  if (selectedDefs.docDefs.length > 0) {
    const docsDir: FileNode = { name: "docs", type: "folder", path: "docs", children: [] };
    const docFolders: Record<string, FileNode> = {};

    selectedDefs.docDefs.forEach((doc) => {
      const folder = doc.folder.replace("docs/", "");
      if (!docFolders[folder]) {
        docFolders[folder] = { name: folder, type: "folder", path: "docs/" + folder, children: [] };
        docsDir.children!.push(docFolders[folder]);
      }
      docFolders[folder].children!.push({
        name: doc.filename,
        type: "file",
        path: doc.folder + "/" + doc.filename,
        content: generateDocTemplate(doc.id, doc.filename),
      });
    });

    files.push(docsDir);
  }

  // obsidian-vault/
  if (config.obsidian.includeVault) {
    const vaultDir: FileNode = { name: "obsidian-vault", type: "folder", path: "obsidian-vault", children: [] };
    
    if (config.obsidian.includeTypedFolders) {
      config.obsidian.noteTaxonomy.forEach((type) => {
        const folderName = type.charAt(0).toUpperCase() + type.slice(1) + "s";
        const typeDir: FileNode = { name: folderName, type: "folder", path: "obsidian-vault/" + folderName, children: [] };
        
        if (config.obsidian.includeTemplateNotes) {
          typeDir.children!.push({
            name: "_template-" + type + ".md",
            type: "file",
            path: "obsidian-vault/" + folderName + "/_template-" + type + ".md",
            content: generateObsidianNote(type, config.basics.projectName),
          });
        }
        vaultDir.children!.push(typeDir);
      });
    }

    if (config.obsidian.includeCanvasStarter) {
      vaultDir.children!.push({
        name: "system-map.canvas",
        type: "file",
        path: "obsidian-vault/system-map.canvas",
        content: JSON.stringify({ nodes: [], edges: [] }, null, 2),
      });
    }

    files.push(vaultDir);
  }

  return files;
}

// Flatten file tree to a list of all file nodes for zip building
export function flattenTree(nodes: FileNode[], acc: FileNode[] = []): FileNode[] {
  for (const node of nodes) {
    if (node.type === "file") acc.push(node);
    if (node.children) flattenTree(node.children, acc);
  }
  return acc;
}
