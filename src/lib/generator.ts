// --------------- .claude/settings.json ---------------
export function generateSettingsJson(config: ConfigState, selectedHookDefs: any[]): string {
  const hooks: Record<string, any[]> = {};

  selectedHookDefs.forEach((hook) => {
    const event = hook.event;
    if (!hooks[event]) hooks[event] = [];
    hooks[event].push({
      matcher: "*",
      hooks: [{ type: "command", command: ".claude/hooks/" + hook.filename }],
    });
  });

  const settings: Record<string, any> = {
    permissions: {
      allow: ["Read", "Edit", "Bash(*)"],
      deny:
        config.core.securityStrictness === "high"
          ? ["Bash(rm -rf*)", "Bash(curl * | bash*)", "Bash(npm publish*)"]
          : config.core.securityStrictness === "medium"
          ? ["Bash(rm -rf*)"]
          : [],
    },
  };

  if (Object.keys(hooks).length > 0) {
    settings.hooks = hooks;
  }

  return JSON.stringify(settings, null, 2);
}

